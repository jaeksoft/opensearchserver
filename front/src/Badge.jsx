import {hot} from 'react-hot-loader/root';
import React from 'react';

const Badge = (props) => {
  if (props.value)
    return (
      <span className="badge badge-dark">{props.true}</span>
    );
  else
    return (
      <span className="badge badge-light">{props.false}</span>
    );
}

export default hot(Badge);

